# ---Strings---

# A string is a collection of characters. "Hello" is a string containing characters - 'H' 'e' 'l' 'l' 'o'.
print("Hello") # This print statement will prints the string "Hello".
print('Hello', '''Akshat''', """Shaw""") # We can use either double qoutes, single qoutes, triple double qoutes or triple single qoutes to surround a string.
print('Hello "Dev"') # We can use qoutes inside strings if we they do not match the outer qoutes surrounding the entire string.

# We can assign strings to variables just like we assign integers or any numeric values to variables
s = "Python"
print(s)

# We can use len() function to get the length of the string
print(len(s)) # Output :- 6

#***** Note :- STRINGS ARE IMMUTABLE.