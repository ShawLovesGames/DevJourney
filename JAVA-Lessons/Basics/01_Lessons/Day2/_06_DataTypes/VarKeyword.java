// ---var Keyword---

public class VarKeyword {
     public static void main (String[] args) { 
          
        var a = 5;
        System.out.println(a);

        /*
            var keyword lets the compiler automatically detect the type of a variable based on the value assigned.
            var works when a value is assigned at the same time (declaring var x; without assigning a value is not possible).
            The type stays the same after is is chosen.
         */

    }
}