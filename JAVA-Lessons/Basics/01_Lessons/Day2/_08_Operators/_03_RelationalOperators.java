public class _03_RelationalOperators {
    public static void main(String[] args) {

        // Relational Operators. Used to compare two or more values.
        int a = 10, b = 5;
        System.out.println("\nRelational Operators:");
        System.out.println("a > b: " + (a > b));
        System.out.println("a < b: " + (a < b));
        System.out.println("a == b: " + (a == b));
        System.out.println("a != b: " + (a != b));

    }
}
