public class _01_AssignmentOperators {
    public static void main(String[] args) {
        
        // Operators are used to perform different types of operations on variables and values.
        // Types of operators:

        // Assignment Operators. Assigns the value on the right to the variable on the left.
        int c = 10;
        System.out.println(c);
        System.out.println(c += 5); // c = c + 5
        System.out.println(c -= 3); // c = c - 3
        System.out.println(c *= 2); // c = c * 2
        System.out.println(c /= 4); // c = c / 4
        System.out.println(c %= 3); // c = c % 3
        System.out.println(c &= 2); // c = c & 2
        System.out.println(c |= 1); // c = c | 1
        System.out.println(c ^= 2); // c = c ^ 2
        System.out.println(c <<= 1); // c = c << 1
        System.out.println(c >>= 1); // c = c >> 1
        System.out.println(c); // Final value of c
        
    }
}
