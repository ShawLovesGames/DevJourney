// ---Comments in JAVA---

public class _4_Comments {
    public static void main(String[] args) {
        System.out.println("Single line comments are written using double forward slash // and multi line comments are written using /* */");
    }
}

// Comments are used to explain java code to any source code reader, without the text effecting the compiler. This is a single line comment.
/*
    Any text written here will be ignored by java.
    This is a multiline comment.
*/