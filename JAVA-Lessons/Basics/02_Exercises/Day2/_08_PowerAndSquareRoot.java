// ---Take a number in a variable and print its square root and it raised to the power of 3---

public class _08_PowerAndSquareRoot {
    public static void main(String[] args) {
        
        int n = 4;

        System.out.println("Square root of " + n + " is " + Math.sqrt(n));
        System.out.println("The cube of " + n + " is " + Math.pow(n, 3));

    }
}