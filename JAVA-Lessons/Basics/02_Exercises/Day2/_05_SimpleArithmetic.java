// ---Take two numbers (store in variables) and print their sum, difference, product, and quotient---

public class _05_SimpleArithmetic {
    public static void main(String[] args) {
        
        int n1 = 75;
        int n2 = 25;
        
        int sum = n1 + n2;
        int diff = n1 - n2;
        int product = n1 * n2;
        int quotient = n1 / n2;

        System.out.println("The sum of the two numbers is: " + sum);
        System.out.println("The difference between the two numbers is: " + diff);
        System.out.println("The quotient when the first number is divided by the second number is: " + quotient);
        System.out.println("The product of the two numbers is: " + product);
        
    }
}