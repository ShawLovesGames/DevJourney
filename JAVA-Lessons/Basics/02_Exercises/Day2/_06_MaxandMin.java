// ---Given two numbers in variables, print the largest and smallest using Math.max() and Math.min()---

public class _06_MaxandMin {
    public static void main(String[] args) {
    
        int n1 = 12, n2 = 8;

        System.out.println("The largest number is: " + Math.max(n1, n2));
        System.out.println("The smallest number is: " + Math.min(n1, n2));

    }
}